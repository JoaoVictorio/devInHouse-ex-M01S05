var cor = "blue";
switch (cor) {
  case "red":
    console.log("Vermelho");
    break;
  case "blue":
    console.log("Azul");
    break;
  case "green":
    console.log("Verde");
    break;
  case "yellow":
    console.log("Amarelo");
    break;
  case "purple":
    console.log("Roxo");
    break;
  default:
    console.log("Cor não encontrada");
}


